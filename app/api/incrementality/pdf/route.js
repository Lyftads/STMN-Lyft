async function renderPdf(html) {
  const token = process.env.BROWSERLESS_TOKEN
  if (!token) return { error: 'BROWSERLESS_TOKEN mancante' }
  let browser
  try {
    const { default: puppeteer } = await import('puppeteer-core')
    const endpoint = process.env.BROWSERLESS_ENDPOINT || 'production-lon.browserless.io'
    browser = await puppeteer.connect({ browserWSEndpoint: `wss://${endpoint}/?token=${encodeURIComponent(token)}` })
    const page = await browser.newPage()
    await page.setContent(html, { waitUntil: 'load', timeout: 30000 })
    await page.emulateMediaType('screen')
    const buf = await page.pdf({ format: 'A4', landscape: true, printBackground: true, margin: { top: '16px', bottom: '16px', left: '14px', right: '14px' } })
    return { buf }
  } catch (e) { return { error: e?.message || 'render error' } } finally { if (browser) await browser.disconnect().catch(() => {}) }
}

export async function GET(req) {
  const { searchParams } = new URL(req.url)
  const days = searchParams.get('days') || '150'
  const loc = (searchParams.get('locale') || 'it').slice(0, 2)
  const t = L[loc] || L.it
  const nloc = { it: 'it-IT', en: 'en-US', es: 'es-ES', fr: 'fr-FR', de: 'de-DE' }[loc] || 'it-IT'

  const data = await internal(req, `/api/incrementality?days=${days}&locale=${loc}`)
  if (!data || !data.ok) {
    return new NextResponse(`<html><body style="font-family:sans-serif;padding:40px;color:#666">${t.noData}</body></html>`, { headers: { 'Content-Type': 'text/html; charset=utf-8' } })
  }

  const html = buildHtml(data, t, nloc)
  const { buf, error } = await renderPdf(html)
  if (!buf) return new NextResponse(html, { headers: { 'Content-Type': 'text/html; charset=utf-8', 'X-PDF-Error': (error || 'unknown').slice(0, 120) } })
  const fname = `LyftAI_Incrementality_${new Date().toISOString().slice(0, 10)}.pdf`
  return new NextResponse(buf, { headers: { 'Content-Type': 'application/pdf', 'Content-Disposition': `attachment; filename="${fname}"` } })
}
