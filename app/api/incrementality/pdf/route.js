async function internal(req, path) {
  try {
    const url = new URL(path, req.url)
    const r = await fetch(url, { headers: { cookie: req.headers.get('cookie') || '' }, cache: 'no-store', signal: AbortSignal.timeout(50000) })
    if (!r.ok) return null
    return await r.json().catch(() => null)
  } catch { return null }
}

