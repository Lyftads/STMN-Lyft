// Platforms that look better on dark background
const BG_DARK = new Set(['tiktok', 'openai'])

export default function PlatformIcon({ platform, size = 20 }) {
  const cdn = CDN_BRANDS[platform]
  const Icon = ICONS[platform]
  if (!cdn && !Icon) return null
  const dark = BG_DARK.has(platform)
  return (
    <span
      title={platform}
      style={{
        display: 'inline-flex',
        alignItems: 'center',
        justifyContent: 'center',
        width: size + 8,
        height: size + 8,
        borderRadius: 7,
        background: dark ? '#000' : 'var(--text)',
        flexShrink: 0,
        boxShadow: '0 1px 2px rgba(0,0,0,0.3)',
      }}
    >
      {cdn ? <CdnLogo slug={cdn.slug} color={cdn.color} domain={cdn.domain} size={size} /> : <Icon s={size} />}
    </span>
  )
}

export function PlatformBadges({ sources = [], size = 18 }) {
  if (!sources?.length) return null
  return (
    <span style={{ display: 'inline-flex', gap: 5, alignItems: 'center' }}>
      {sources.map(p => <PlatformIcon key={p} platform={p} size={size} />)}
    </span>
  )
}
