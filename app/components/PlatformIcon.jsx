const BG_DARK = ['tiktok', 'openai']

export default function PlatformIcon({ platform, size = 16 }) {
  const Icon = ICONS[platform]
  if (!Icon) return null
  const dark = BG_DARK.includes(platform)
  return (
    <span
      title={platform}
      style={{
        display: 'inline-flex',
        alignItems: 'center',
        justifyContent: 'center',
        width: size + 6,
        height: size + 6,
        borderRadius: 6,
        background: dark ? '#000' : '#fff',
        flexShrink: 0,
      }}
    >
      <Icon s={size} />
    </span>
  )
}

export function PlatformBadges({ sources = [], size = 14 }) {
  if (!sources?.length) return null
  return (
    <span style={{ display: 'inline-flex', gap: 4, alignItems: 'center' }}>
      {sources.map(p => <PlatformIcon key={p} platform={p} size={size} />)}
    </span>
  )
}
